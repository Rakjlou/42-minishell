#include "libft.h"

long	ft_atol(const char *nptr)
{
	long	result;
	int		multiplier;

	result = 0;
	multiplier = 1;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == ' ')
		nptr++;
	if (*nptr == '+' || *nptr == '-')
	{
		if (*nptr == '-')
			multiplier = -1;
		nptr++;
	}
	while (ft_isdigit(*nptr))
	{
		result = (result * 10) + (*nptr - '0');
		nptr++;
	}
	return (result * multiplier);
}
